package com.ceresdata.pojo;

public class Formart1 {

    private short pos;
    private Integer fre;
    private byte mode;
    private byte check;
    private short len;
    private String time;
    private short id;
    private String path;

    public Formart1() {
    }
    public short getPos() {
        return pos;
    }

    public void setPos(short pos) {
        this.pos = pos;
    }

    public Integer getFre() {
        return fre;
    }

    public void setFre(Integer fre) {
        this.fre = fre;
    }

    public byte getMode() {
        return mode;
    }

    public void setMode(byte mode) {
        this.mode = mode;
    }

    public byte getCheck() {
        return check;
    }

    public void setCheck(byte check) {
        this.check = check;
    }

    public short getLen() {
        return len;
    }

    public void setLen(short len) {
        this.len = len;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public short getId() {
        return id;
    }

    public void setId(short id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }



}
